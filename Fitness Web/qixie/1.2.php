<p>　　所以，为因应器材及场地的不便，解决选手的练习问题，也让一般人也能享受划船训练的优点，现在的健身房里几乎都配有【室内划船机】供大众使用！</p>
<p>　　可别小看这台划船机所带来的训练效益！不管是拿来热身、当作有氧运动，或是规划成间歇训练，划船机都能让你多个愿望一次满足。</p>
<p>　　<span style="color: rgb(255, 0, 0);"><strong>划船机动作技巧：</strong></span></p>
<p>　　首先，划船得靠你的双脚，运用瞬间爆发力作推蹬，以利下半身的带动，并配合上半身稳定、背夹，最后再用双手顺势拉杆；待双手回到身体前面，双脚才会再推回原本位置。</p>
