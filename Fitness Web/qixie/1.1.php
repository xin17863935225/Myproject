<p>&nbsp;负重耸肩：杠铃负重耸肩动作图解教程</p>
<p>健美爱好者无不希翼着能够拥有足以撑起衬衫的、强健的肌肉块。这不仅需要有发达的胸肌、背肌和肩部肌肉，而且还要拥有健壮的斜方肌。</p>
<p style="text-align: center;">&nbsp;</p>
<div style="text-align: center;"><img  src="images/负重耸肩2.jpg" width="572" border="0" height="287" alt="" style="cursor:pointer" onclick="window.open('/uploads/allimg/170602/4_170602153618_1.jpg')" /></div>
<div>&nbsp;</div>
<div>斜方肌训练比一般健美训练都要简单，其主要动作就是<strong>耸肩（Shrug）</strong>，也就是两肩向两耳方向上抬，做耸肩练习需要的器械也只需一对哑铃或一个杠铃。强化斜方肌的其他训练：普通硬拉和直立划船。</div>
<div>&nbsp;</div>
<div><span style="color: rgb(255, 0, 0);"><strong>目标锻炼部位：</strong></span><strong>斜方肌 &nbsp;（还涉及颈肌、上背肌群）</strong></div>
<div>&nbsp;</div>
<div><span style="color: rgb(255, 0, 0);"><strong>动作要领：</strong></span></div>
<div style="text-align: center;">&nbsp; &nbsp;</div>
<div style="text-align: center;"><img  src="images/负重耸肩.gif" width="496" border="0" height="287"  /></div>
<div>&nbsp;</div>
<div>1.自然站立，两手背向前，持杠铃或哑铃，下垂在腿前。</div>
<div>&nbsp;</div>
<div>2.两肩同时向上耸起，使肩峰尽量触及耳朵，然后在这个顶点位置上慢慢地使两肩向后转，再慢慢由后向下转至两臂下垂的原位。重复做。在耸肩过程中，不要曲肘。</div>
<div style="text-align: center;">&nbsp;<img  src="images/负重耸肩3.gif" width="118" border="0" height="97" /></div>
<div>&nbsp;</div>
<div><span style="color: rgb(255, 0, 0);"><strong>注意事项：</strong></span></div>
<div>&nbsp;</div>
<div>1.耸肩训练的原则是：不要太轻，更不能太重。合理的重量应该是，你可以严格地按照要求完成整个动作，即两肩尽量向两耳靠近，保持一会儿，不要屈肘。然后，两肩同时下降到最低点。整个动作可以做10-12个重复。</div>
<div>&nbsp;</div>
<div>2.如果你使手腕稍屈，并使两肘尖向外转，这对肩侧斜方肌的收缩效果更有效些</div>
<div>&nbsp;</div>
<div>3.斜方肌训练策略：与其他身体部位一样，要尽量使动作多样化，切勿动作单一化。尝试完多种耸肩动作后，包括传统的耸肩以及直立划船，选出最有效的几个动作集中进行训练。坚持将杠铃和哑铃作为斜方肌训练的基础，拉力器等动作作为辅助选择。</div>
<div>&nbsp;</div>
<div>4.传统的哑铃和杠铃耸肩和直立划船，主要锻炼斜方肌的上部，并不能直接锻炼中部和下部斜方肌。为了更好地锻炼整个斜方肌，以下介绍的10种创新性的耸肩动作来达到。打破斜方肌训练一贯的陈旧乏味。</div>
<div>&nbsp;</div>
<div><strong>（1）背后耸肩</strong></div>
<div>&nbsp;</div>
<div style="text-align: center;"><img  src="images/负重耸肩4.gif" width="320" border="0" height="240" alt="" style="cursor: pointer;" /></div>
<div>&nbsp;</div>
<div>也称之为&ldquo;哈尼耸肩&rdquo;，因八届奥 林匹亚先生李&middot;哈尼而得名。</div>
<div>将杠铃置于身后而不是大腿前面，这样会迫使你在整个耸肩过程中都向后拉动肩胛骨，因此可更多地锻炼斜方肌中部(尽管大部分压力仍在斜方肌上部)。在史密斯机上做这一动作效果会更好。</div>
<div>&nbsp;</div>
<div><strong>（2）上斜耸肩</strong></div>
<div>&nbsp;面朝上坐在上斜平凳上，平凳与地面成60度角或更高。双手各握一哑铃，双臂与地面垂直(与上斜哑铃弯举的初始姿势相似)。然后耸肩。与上半身垂直于地面的耸肩相比，这会对斜方肌产生一个不同角度的冲击。</div>
<div>&nbsp;</div>
<div><strong>（3）俯卧耸肩</strong></div>
<div>&nbsp; &nbsp; 这一动作与上斜耸肩正好相反。面朝下趴在上斜平凳或高平凳上，双手握一杠铃或两个哑铃，双臂伸直，且与地面垂直。不要向耳部方向耸肩，而是在保持双臂伸直的同时通过收缩肩胛骨提起负重。这可锻炼斜方肌中部和上半背部。</div>
<div>&nbsp;</div>
<div><strong>（4）小腿训练机耸肩</strong></div>
<div>&nbsp;&nbsp;</div>
<div>站在小腿训练机上，使肩膀位于垫下，然后耸肩，与负重耸肩的方式一样。由于双臂不需要在身侧抵挡抗力，肘部和腕关节也不会受到一丁点压力。这一训练还能使每次动作都锁定在同一运动轨迹上。</div>
<div>&nbsp;</div>
<div style="text-align: center;"><img src="images/负重耸肩5.gif" width="160" border="0" height="120" alt="" style="cursor: pointer;" />&nbsp;&nbsp;<img  src="images/负重耸肩6.gif" width="160" border="0" height="120" alt="" style="cursor: pointer;" /><br />
&nbsp;</div>
<div>&nbsp;</div>
<div><strong>（5）耸肩顶峰收缩</strong></div>
<div>&nbsp;每次耸肩动作到达顶部位置时，保持3～5秒钟，可明显增加耸肩强度。类似的顶峰收缩对耸肩来说特别有效。与许多推举不同，耸肩运动时收缩关节不会锁死，而且由于双臂与地面垂直，重力向下的拉力会达到最大。</div>
<div>&nbsp;</div>
<div><strong>（6）头上耸肩</strong></div>
<div>&nbsp; 尽管这一独特的动作既可以使用杠铃也可以使用两个哑铃，但为了安全和便宜我们还是推荐使用史密斯机。开始时杠铃位于头顶，就像肩部推举的顶端姿势那样，只是肩胛骨要向下拉的尽可能远。然后肩胛骨向上尽可能高的推起杠铃。注意力要全部放在肩胛骨的上下运动上。</div>
<div>&nbsp;</div>
<div><strong>（7）单臂耸肩</strong></div>
<div style="text-align: center;">&nbsp;&nbsp;<img  src="images/负重耸肩10.gif" width="160" border="0" height="120" alt="" style="cursor: pointer;" /></div>
<div>尽管使用的是哑铃，大多数健身者还是同时对两个肩膀进行耸肩训练。然而，斜方肌也可以像其他肌肉那样受益于单侧训练。抓住连接在低位拉力器上的D形把手，做完一组的所有次数后再转换到另一只手，或者双手各握一哑铃或把手，左右斜方肌交替进行训练。单侧耸肩可以有重点地锻炼每一侧，并增加动作幅度。在健身房中单侧训练并不是一种普遍的锻炼方法，但是对斜方肌却很有帮助。</div>
<div>&nbsp;</div>
<div><strong>（8）史密斯机耸肩</strong></div>
<div style="text-align: center;">&nbsp;</div>
<div>无论是做背后耸肩、头上耸肩还是以传统的双臂向下、杠铃在前的方式做耸肩，史密斯机都可以将耸肩动作锁定在一个竖直路径上，同时还能够轻松地改变身体位置。例如，在做耸肩动作时可以向后并微弯腰部来更多的训练斜方肌下部，也可以用史密斯机做单臂耸肩。</div>
<div>&nbsp;</div>
<div><strong>（9）低位拉力器耸肩</strong><span style="text-align: center;"><img  src="images/负重耸肩7.gif" width="320" border="0" height="240" alt="" style="cursor: pointer;" /></span></div>
<div>&nbsp; &nbsp; 抓住连接在低位拉力器上的握把向后站，使拉力器与地面约成45度角，同时向后向上耸肩。可同时锻炼到斜方肌的上部和中部。</div>
<div>&nbsp;&nbsp;</div>
<div><strong>（10）交叉拉力器耸肩</strong></div>
<div>&nbsp;站在交叉拉力器的中间，抓住连接在左右拉力器上的D形把手。拉力器应该设在最低位置。耸肩，并保持双臂伸直，与身体成45度角外张。双臂的位置和压力的方向除了能够提升肩胛骨外(锻炼斜方肌上部)，还可以更容易地内拉肩胛骨(锻炼斜方肌中部)。</div>
