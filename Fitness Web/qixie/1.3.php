<p>　　<strong>TRX划船</strong></p>
<p>　　利用TRX做划船训练，负担自身体重，却能减少下背负担，使用更多肩胛骨后夹的动作，帮助你感受背部肌群收缩。</p>
<p>　　<b>难度</b>:由简到难&mdash;&mdash;站立&rarr;仰卧&rarr;仰卧垫高或单手&mdash;&mdash;负重！</p>
<p>　　<strong>首先</strong>，在单杠上挂上附有手把的吊带，手把离地约1米。双手间距与肩同宽，正手握住吊带。</p>
<p>　<strong>　身体姿势</strong>：脚尖朝上、脚跟着地，收紧腹部及臀部>，肩、背、髋、腿尽量呈一直线，保持躯干稳定。</p>
<div style="text-align: center;"><img  src="images/TRX划船3.gif"width="350" border="0" height="231" style="cursor:pointer"/></div>
<p>　　动作过程上拉时，肩关节先做后移、固定，将背部收起（肩胛骨夹），手再接着拉（手肘保持微微内夹），感受背部肌肉紧缩。让胸口靠近把手后，再利用背部力量控制下放速度，感受背部肌肉被拉长，直到双手伸直即可。</p>
<p>　　<span style="color: rgb(255, 0, 0);"><strong>锻炼次数：10-15个*4组。</strong></span></p>
<div style="text-align: center;"><img  src="images/TRX划船4.jpg" width="437" border="0" height="321" alt="" style="cursor:pointer" /></div>
<p>　<strong>　背部发力技巧</strong></p>
<p>　　原则上，反向划船也会用到二头手臂、肩部及核心稳定的力量，会有轻微的借力是正常现象。但可能少部分人会错把意识放在肩部，出现耸肩、斜方或二头肌酸痛的后果。</p>
<p>　<strong>　</strong><span style="color: rgb(255, 0, 0);"><strong>改善方式</strong></span></p>
<p>　　按起始动作先放松肩部，进行两段式动作。</p>
<p>　　<strong>第一步，</strong>一样先做好预备动作，接着先感受背部收缩夹起；</p>
<p>　　<strong>第二步</strong>再用双手跟着拉。简单来说，就是先做背部收缩，手再跟着拉的步骤，用意识来控制速度，会比较好感受训练效果！</p>
<p>　　如果你的核心肌群比较弱，也能先从膝盖弯曲的反向划船做起，专注在背肌的控制上，等到能力达到一定程度，再改回原来的动作。</p>
