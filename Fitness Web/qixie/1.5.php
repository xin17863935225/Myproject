<div>　<strong>　3、壶铃</strong></div>
<div>　　为铸铁制成，因其形状如提壶，所以名为壶铃。由一铁球和半环形握柄组成。其重量有10公斤、20公斤、25公斤、30公斤之分。也可用民间使用的石锁代替。</div>
<div>　　<strong>4、拉力器</strong></div>
<div>　　拉力器种类繁多，一般有弹簧拉力器、橡皮条技力器和重锤滑轮拉力器等。弹簧拉力器&mdash;&mdash;比较常用，一般有5条钢丝弹簧和两个木柄组成。其拉力很大，每条约有6公斤的拉力。 橡皮条拉力器&mdash;&mdash;也可制成如弹簧拉力器的样式，可用一条3米左右的橡皮条，在其两头固定上把柄，较便于携带， 实用性好。其规格和种类可根据需要自己设计制作。重锤滑轮拉力器&mdash;&mdash;是由握柄、钢丝绳、重锤片和固定滑轮组成。一般都是固定在墙上或特制的金属架上使用，式样有单臂和双臂使用之分。</div>
<div>　<strong>　5、弹簧棒</strong></div>
<div>　　弹簧棒的两端为木柄，中间是一根弹性很大的弹簧，用来训练上臂和躯干的肌群。</div>
<div>　<strong>　6、卷力器</strong></div>
<div>　　卷力器是用来发达前臂肌的专用工具，对于增强握力效果也很好。常用的卷力器有两种，即：手握式卷力器&mdash;&mdash;用棍制成，直径约为0035米，长达0．3～0．4米，中间打一孔，穿入绳子，绳长1米，绳子的一端结扎在木棍上，另一端结扎上杠铃片或其它重物即可；支架式卷力器&mdash;&mdash;制作方法同上，只是把横棍放在两个支架上进行练习，用起来方便，器械固定性强，随时可以用。</div>
