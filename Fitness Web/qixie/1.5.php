<div>　　<strong>健身房常见无氧器械介绍</strong></div>
<div>　　<strong>1、杠铃</strong></div>
<div>　　杠铃有标准杠铃和非标准杠铃两种：标准杠铃&mdash;&mdash;由横杠、杠铃片和卡箍三部分组成。比赛用的是国际标准杠铃，按规定横杠总长度不超过入20米，直径0．28米，两个内卡箍之间的距离为1．31米，最大的杠铃片直径为0.45米。</div>
<div>　　杠铃片重量、颜色如下：50公斤（绿色）；25公斤（红色）； 20公斤（蓝色）；15公斤（黄色10、5、2.5公斤（白色）；1．05 公斤和0．25公斤。非标准杠铃&mdash;&mdash;结构同于标准杠铃，尺寸要求并不严格，制作要求不高，重量可以自由确定，用民间使用的石担也可以代替。此外，为达到某些特殊要求，如需发展某局部的肌肉，可按需要制作各种形态的特种杠铃（如屈轴杠铃，环型杠铃）。</div>
<div>　　<strong>2、哑铃</strong></div>
<div>　　哑铃有固定和调节两种：固定哑铃&mdash;&mdash;分为轻重两种，轻哑铃重量为6、8、12、16磅（一磅等于0．45公斤）等，重哑铃有10、15、20、25、30公厅等。调节哑铃&mdash;&mdash;形状稍不同于固定哑铃，为把柄两端装上不同重量的圆铁片而成，这些圆铁片可以根据需要随意增减，这种哑铃还可以用一根圆铁棍穿过小杠铃片，成为两手握很式哑铃。</div>
