                            <p>&nbsp;　　众所周知，哑铃是一种简单、经济的健身器材，但是很少有人深究其健身的原理。现在大众对哑铃健身作用的认识仍有不少误区，哑铃的健身功效，远远没有得到充分的发挥。</p>
<p><strong>误区一：用哑铃锻炼，既可增加力量，又可健美形体</strong></p>
<p>　　科学地使用哑铃，确实可以收到很好的锻炼效果。有材料说，当年施瓦辛格一身健美的肌肉，主要就是通过哑铃锻炼而得。但是确有不少人用哑铃锻炼后，既没有增加力量，也没有变得健美，常会就此心灰意冷，哑铃也被束之高阁，甚至成为锤子的替代品。事实上，哑铃健身大有学问。如果不加以贯彻，锻炼效果往往会差强人意。</p>
<p>　　在锻炼前，我们首先需要明确的是，力量与健美究竟谁是自己的最爱。锻炼肌肉主要通过两条途径，一是增加肌纤维的数量;二是增加肌纤维的体积。第一条途径可以提高锻炼者的力量，但健美效果相对不明显，适于专业举重运动员;第二条途径可使肌肉体积显着增大，但是力量增加相对较少，适合于健美运动员或者大众健身者。通常健身爱好者都把健美视为主要目的，出于这一目的用哑铃锻炼时，须遵循如下规则。</p>
<p>　　训练前要先选择合适重量的哑铃。一般需要选择65%&mdash;85%负荷的哑铃，所谓负荷是指所能举起的最大重量，举个例子，如果每次能举起的最大重量是 10千克，就需要选择重量为6.5&mdash;8.5千克的哑铃进行锻炼。对于一般健身者而言，拥有两三付不同重量的哑铃并坚持锻炼，就足够了。锻炼时每次举6&mdash;8 组，每组重复8&mdash;12次，动作速度不宜过快，每组间隔2&mdash;3分钟。负荷太大或太小，间歇时间太短或太长，效果都不好。</p>
<div style="text-align: center;"><img  src="images/哑铃健身三个误区.jpg" width="402" border="0" height="511" /></div>
<p><strong>误区二：哑铃只练上肢</strong></p>
<p>　　有人认为哑铃只能练出健美的上肢，要想全面锻炼，可能需要某些更复杂的器械。</p>
<p>　　时下一些较为昂贵的联合健身器械不仅在健身房里比比皆是，有的甚至进入了日常家庭。尽管联合器械具有其他简单器械无可比拟的优点，但是也存在价格高、占地面积大、挪动不方便等缺点，这些缺陷正好是哑铃的优点，而且只要设计和计划得当，哑铃训练完全可以获得与使用联合器械类似的功效，何乐而不为呢?</p>
<p>　　锻炼上肢肌肉是哑铃的拿手好戏，但是哑铃同样可以锻炼腰腹肌肉。如在仰卧起坐的时候在颈后部双手紧握哑铃，可以增加腹肌练习的负荷，提高锻炼效果; 进行背屈伸练习时，同样可以利用哑铃增加背部肌肉的负荷，以刺激背肌的增长;手握哑铃的体侧屈或转体运动，可以锻炼腹内、外斜肌;手握哑铃的直臂前举、侧平举等可以锻炼肩部和胸部肌肉;此外，哑铃还可以锻炼下肢肌肉，如手持哑铃单脚蹲起、双脚蹲跳等等。</p>
<p><strong>误区三：哑铃不适合老年人</strong></p>
<p>　　老年人中有不少健身爱好者，但他们常会选择慢跑、广播操或太极拳等项目，力量训练则很少有人问津，哑铃更是基本上不被考虑。一般人都以为，年纪大了，力量下降了，所以不适合进行力量训练了。事实并非如此，从某种意义上说，正因为力量下降了，所以更需要力量训练。</p>
<p>　　肌肉产生运动，同时也帮助身体维持静止的状态。随着年龄的增长，肌纤维自然萎缩，力量下降，不仅动作迟缓，而且稳定性降低，肌腱与骨膜更易发生过度磨损，所以各关节发生疼痛的可能性也大大增加。适宜的力量锻炼，不仅可以延缓老年人肌肉萎缩的进程，保持肌肉弹性，为更好地从事其他形式的健身活动打基础，而且还可以增加肌肉对各关节稳定性的保护作用，以减少或缓解各类疼痛。哑铃小巧玲珑、物美价廉，特别适合老年人进行力量锻炼。</p>
