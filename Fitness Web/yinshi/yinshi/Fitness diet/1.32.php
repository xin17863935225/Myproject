<P>　　我们可以从营养表中看到，他购买的糙米每45g含有碳水35g，碳水含量78%，而每50g香米中含碳水40g，碳水含量80%，以高碳日600g为例，他需要吃769.23g糙米，或者750g香米，需要注意的是营养表Serving Size旁边的（dry），翻译成中文就是干燥的意思，顾名思义，糙米和香米的质量也要按照生米来称量。</P>
<P>　　②蛋白质：这个备赛期间，他蛋白质摄入来源于，瘦牛肉、鸡胸肉、鱼肉，牛肉是他的最爱，但因为牛肉脂肪含量较高，在备赛前一个月左右就停止了牛肉的摄取。</P>
<DIV style="TEXT-ALIGN: center"><IMG style="CURSOR: pointer" onClick="window.open('/uploads/allimg/170329/8_170329100240_1.jpg')" border=0 alt="" src="../../图片/9.jpg" width=550 height=388></DIV>
<P>　　之后的蛋白质摄入都源于鱼肉，白色的鱼肉，其他颜色的如三文鱼、金枪鱼也是不能吃的，在他的饮食计划中，每天摄入6顿蛋白质每顿80盎司，而蛋白质的计算与碳水的相反，蛋白质的重量是算煮熟后的，由于赛前一周他的碳水降到了每天200g，非常容易感到饿，于是在教练的同意下，蛋白质的摄入加到了每顿110盎司。</P>
