<P>　　①碳水循环：周期4天，比例2:4:4:6，即第一天200克、第二天400克，第三天400克、第四天600克，这里的碳水重量不等于食物重量，并不是说一天吃200g的米饭就可以，例如糙米和香米是他碳水的主要来源。</P>
<DIV style="TEXT-ALIGN: center"><IMG style="CURSOR: pointer" onClick="window.open('/uploads/allimg/170329/8_170329100112_1.png')" border=0 alt="" src="../../图片/1.png" width=584 height=370></DIV>
