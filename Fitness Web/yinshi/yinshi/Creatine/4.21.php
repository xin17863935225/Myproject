<P>　　尽管有广泛的科学证明，人们依旧对于肌酸有无数的疑问。</P>
<P>　　让我们一起来听听<STRONG>《肌酸报告》</STRONG>的作者 Nick Tumminello 教练特别为此澄清这些问题。</P>
<P>　　<STRONG>从1832年</STRONG>起就有许多关于肌酸的疑问，那年有一个疯狂的法国化学家 Michael Cheveul 在骨骼肌中发现了一种酸。</P>
<P>　　<STRONG>160 年后</STRONG> - 单一水合肌酸在 1992 年被陈列在营养补给品陈列架上。此后，人们为肌酸宣嚷不已，对于肌酸的疑问越来越多：家长怀疑肌酸是否安全，消费者好奇肌酸是怎么运作的，举重人想着该在什么时候吃、该配多少水。</P>
<P>　　<STRONG>一如《肌酸报告》的作者所述 </STRONG>- 一本免费、公正、详细的肌酸评测的书刊 - 我花了许多时间阅读科学研究中有关肌酸的细节，也跟许多世界上具有领导地位的补给品专家谈论，所获得的结论可以回答你对肌酸的一切疑问。我对我在蛋白质上的研究也做了相同深入的功夫，这些也同样是提供给大家，完全免费的资讯。</P>
<P>　　如果你希望你的问题得到简单明了的解答，你来对地方了。我会告诉你对于肌酸你应该知道的所有细节：肌酸是什么、怎么用、安全性如何、怎么填充、副作用等等。</P>
<P style="TEXT-ALIGN: center"><IMG style="TEXT-ALIGN: center; FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/54.jpg" width=561 height=375></P>
<P>　　<STRONG>1.什么是肌酸？</STRONG></P>
<P>　　肌酸是含氮有机酸，从氨基甘氨酸、精氨酸和甲硫氨酸中而来。骨骼肌包含 95% 的肌酸，而心肌、大脑、和激素中含有 5%。</P>
<P>　　<STRONG>2.肌酸的作用是什么？</STRONG></P>
<P>　　根据 美国肌力与体能训练协会（NSCA） 的高级培训师 David SandlerSenior所述："肌酸能让你有更长、更强的运动能力，能帮助你训练时多做几下。补充肌酸能增进磷酸肌酸的储存多出 10~40%。"</P>
<P>　　<STRONG>3.肌酸为什么有效？</STRONG></P>
<P>　　根据 Jose Antonio 博士 - 诺瓦东南大学（Nova Southeastern） 的教授，以及运动营养学会ISSN（International Society of Sports Nutrition）的CEO，他表示"肌酸如同燃料供应一般，让你能透过增进磷酸肌酸的储存进而快速的运动。"</P>
