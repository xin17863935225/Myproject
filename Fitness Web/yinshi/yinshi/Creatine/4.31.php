<P>　<STRONG>　最彻底的健身补剂――肌酸！</STRONG></P>
<P>　　即使你不是一个运动员，但你肯定听说过"肌酸"――它被称为最彻底的补剂</P>
<P>　　肌酸是人体中一种含氮的有机酸，能够辅助为肌肉和神经细胞提供能量。正是因为肌酸是机体中固有的物质，所以它不存在任何副作用。任何国际大赛对肌酸也不会禁止，可见其安全性。更有很多专业运动营养文章把肌酸称为"迄今研究得最透彻最安全和有效果的补剂"。</P>
<P>　　关于肌酸你肯定会有很多问题！让我们一起来看看！肌酸使用常见问题解析</P>
<P>　<STRONG>　Q：健身必须服用肌酸吗？</STRONG></P>
<P>　　A：如同任何其他补剂一样，补剂无法代替饮食，肌酸不是神奇药水，更不是大力水手的菠菜！</P>
<P>　　肌酸的主要作用：</P>
<P>　　提高运动表现，包括爆发力、耐力等，增强训练效果；</P>
<P>　　同时肌细胞在吸收肌酸时，也带给细胞更多的水份（水合作用），细胞体积由此增大，增加肌蛋白的合成速度，促进了新肌的生长</P>
<P style="TEXT-ALIGN: center"><IMG style="TEXT-ALIGN: center; FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/60.jpg" width=561 height=375></P>
<P>　<STRONG>　Q：我需要多少肌酸？</STRONG></P>
<P>　　A：人每天消耗的肌酸，取决于自身体重以及肌肉量。正常情况下，一个 70 kg 体重的成年人，每天大约需要消耗 2 g 左右的肌酸。这些肌酸，一部分靠食物摄取，一部分靠身体自身合成。</P>
<P>　　但是，当你想要增加力量或提升运动表现时，单纯依靠身体合成肌酸，效率很低；这时候，就需要额外的摄取。某些食物，尤其是红肉和鱼类，富含肌酸，但相比于蛋白质，靠食物大量摄取肌酸，其实效率不是特别高</P>
<P><SPAN style="FONT-SIZE: 14px">　　事实上，除去那些健美运动员，人们平均每天通过饮食摄入的肌酸，只有 1 g 左右，加上身体自身合成 1 g 左右 (1)。如果你想通过饮食大量摄取肌酸，比较困难――这时候，就需要额外的肌酸补剂。</SPAN></P>
<P>　　有意思的是，尽管大多数厂商宣传每日的肌酸摄入大约在 3 - 5 g，学术界并没有对"推荐摄入量"有一个明确的答案。一般来说，每日摄入 2 - 20 g 都是安全的</P>
