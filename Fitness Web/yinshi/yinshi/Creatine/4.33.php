<TD><DIV style="TEXT-ALIGN: center"><IMG style="CURSOR: pointer" onClick="window.open('/uploads/allimg/151117/4_151117172626_2.jpg')" border=0 alt="" src="../../图片/63.jpg" width=565 height=263></DIV>
<P>　<STRONG>　Q：肌酸需要冲击期吗？</STRONG></P>
<P>　　A：所谓冲击期，是指在最开始的 1 - 2 周，每日摄取大量（一般为 15 - 20 g）的肌酸，造成肌肉通过水分滞留迅速增长，之后再减至每天 3 - 5 g 的摄入量。这种方法起源于 1996 年的研究 ，并在健身界逐渐风行。不过，近期有很多研究表示，从长期角度看，使不使用冲击期，对于肌肉量的增长并没有区别。影响肌肉增长量的，是平均每天摄入的肌酸总量。</P>
<P>　　当然，如果你还是迷信冲击期，这么吃对身体并没有什么坏处。</P>
<P style="TEXT-ALIGN: center"><IMG style="TEXT-ALIGN: center; CURSOR: pointer" border=0 alt="" src="../../图片/64.jpg" width=341 height=395></P>
<P>　<STRONG>　Q：肌酸可以和其他食物一起吃吗？</STRONG></P>
<P>　　A：完全可以。肌酸可以被冲泡在水、牛奶、果汁、蛋白粉饮料……里，一起服用。</P>
<P>　　很多实验表明，和碳水化合物一起服用，可以让肌酸的效果更佳。</P>
<P><SPAN style="FONT-SIZE: 14px">　　</SPAN>另外，由于肌酸的水合作用，摄入肌酸要保持足够的水分！才能作用尽显</P>
<P>　　<STRONG>Q：什么时候吃肌酸比较好？</STRONG></P>
<P>　　A：肌酸的服用时间没有限制，只要注意不要和咖啡同服，不要过量服用，就没有问题。</P>
<P>　<STRONG>　Q：肌酸有依赖性吗？</STRONG></P>
<P>　　A：肌酸在体内的滞留时间大约为 3 - 5 周，假设你停止摄入肌酸超过这个时间，体重和纬度可能 会下降，但这只是因为肌肉中的水分流失，而非肌肉本身损失（当然，如果不锻炼，肌肉损失是正常的）。肌酸不同于类固醇等激素，不会有任何依赖性。</P>
