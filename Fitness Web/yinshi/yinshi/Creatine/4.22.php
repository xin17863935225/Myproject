<P>　　<STRONG>4.谁需要肌酸？</STRONG></P>
<P>　　简单说，如果有人想要增加纯肌肉、增加力量、加强在无氧运动时的表现，就可以考虑肌酸。</P>
<P>　　Antonio 博士说，"时至今日，肌酸是最好的增强无氧运动表现的膳食补给，并且能增加纯身体质量、加大肌纤维体积。"</P>
<P>　　<STRONG>5.肌酸对练肌肉有帮助吗？</STRONG></P>
<P>　　可以，研究结果表示肌酸有以下助益，</P>
<P>　　<SPAN style="COLOR: rgb(255,0,0)">增加无脂肪身体成分</SPAN></P>
<P><SPAN style="COLOR: rgb(255,0,0)">　　使力量更增强（以 1RM 的重量来测试获得的结果）</SPAN></P>
<P><SPAN style="COLOR: rgb(255,0,0)">　　增加肌肉耐力</SPAN></P>
<P><SPAN style="COLOR: rgb(255,0,0)">　　增加无氧时的力量和表现（在许多活动中都得到证实，包括连续跳跃、蹲跳、膝伸展和折返跑）</SPAN></P>
<P style="TEXT-ALIGN: center"><IMG style="TEXT-ALIGN: center; FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/56.jpg" width=492 height=393></P>
<P>　<STRONG>　6.肌酸能帮助减脂吗？</STRONG></P>
<P>　　当然，肌酸帮助你获得并保持代谢旺盛的肌纤维，这间接促进了脂肪的燃烧。</P>
<P>　　进一步简单来讲，你身上的肌肉越多，你在健身房里就能练的越强，且在训练期间甚至训练后都能燃烧更多的卡路里。</P>
<P>　　此外，肌酸的水合作用（hydration properties）特性也有助于更直接的提升代谢能力，Antonio 博士说"充分含水的细胞代谢能力会更好"。</P>
<P>　<STRONG>　7.我应该摄取多少肌酸？</STRONG></P>
<P>　　在维持期当中，每天吃 3~6克即可。</P>
<P>　　<STRONG>8.肌酸填充期是什么？</STRONG></P>
<P>　　为了尽可能发挥最大效果，在头 7 ~ 14 天，每天吃 10-20 克，这就是所谓的填充期，这样能够让肌酸尽快发挥作用。</P>
<P>　　经过填充期后，就回到问题 7 的维持期，一天 3 ~6克。</P>
