<P>　<STRONG>　健身好帮手――</STRONG><STRONG style="FONT-SIZE: 14px">肌酸</STRONG></P>
<P>　　除了蛋白补充剂之外，肌酸(Creatine)可算是最出名的健身补充用品。为什么呢？进来看看介绍吧！</P>
<P>　　肌酸的功用</P>
<P>　<STRONG>　1. 增加肌肉量</STRONG></P>
<P>　　肌酸补充剂在健美界及运动界广受欢迎，因为肌酸能够提高细胞中的水份，让肌肉增大，此外亦有研究指出肌酸使卫星细胞(Satellite Cell)更活跃，提升肌肉纤维生长的速度及帮助肌肉复元。</P>
<P>　　<STRONG>2. 加强脑部对肌肉的控制</STRONG></P>
<P>　　除了肌肉受惠，原来脑袋的表现亦因为肌酸的存在而提升，有研究证明肌酸可缩短反应时间、提升平衡力及改善心情。</P>
<P style="TEXT-ALIGN: center"><IMG style="TEXT-ALIGN: center; FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/52.jpg" width=341 height=395></P>
<P>　<STRONG>　3.让肌肉储存更多能量</STRONG></P>
<P>　　肌肉依靠ATP-PC进行快速及爆发性动作，不过肌肉只能储存少于十秒肌肉收缩所需之ATP，因此ATP必须不断补充以维持运动。</P>
<P>　　过程中磷酸肌酸(PC)失去一个高能磷酸根以使ATP再次合成；因此，肌酸担任一个能量再生者的角色，当人体于瞬间需要大量能量时，肌肉内之肌酸越多，人体便具有更大的潜在能量。</P>
<P>　<STRONG>　4.增强爆发力及肌力</STRONG></P>
<P>　　肌酸的补充能增加肌肉中磷酸肌酸的贮存量，提高运动后恢复期磷酸肌酸再合成速率，并缩短运动后恢复期的时间。众多学者已证实肌酸能增强爆发力及肌力这个事实。此外，肌酸不但可令你具更强的爆发力及肌力，同时并可延缓疲劳的发生。</P>
<P>　　<SPAN style="COLOR: rgb(255,0,0)">同时使用肌酸及乳清蛋白(Whey Protein)增加肌肉量</SPAN></P>
<P>　　临床实验证实同时服用肌酸及乳清蛋白能有助提升肌肉生长：17位有重量训练习惯的男士，被分成2组，第一组在训练前及后服用乳清蛋白、肌酸及葡萄糖；第二组则在早餐前及晚上服用相同组合的补充剂。</P>
<P>　　实验维持10星期，第一组的男士无论在肌肉量及降低体脂的表现均比第二组男士出色，因此在训练前后服用肌酸及乳清蛋白有助增加肌肉量！</P>
