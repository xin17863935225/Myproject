<P>　<STRONG>　Q：肌酸摄入越多越好？</STRONG></P>
<P>　　A：和任何营养素一样，人体能承受的肌酸量有限度，一般来说，每天摄入大于 20 g，就已经没什么意义了。人体肌肉组织最大的肌酸承载量大约是 0.3 g/ kg (0.14 g/ lb) (3)，加上每日消耗的数量，也就是说，一个 70 kg 左右的成年男性，他最大的肌酸承载量不会超过 25 g。</P>
<P>　　<STRONG>Q：肌酸伤肾吗？</STRONG></P>
<P>　　A：绝大多数的研究都表明，无论是短期（以天和周为单位）还是长期（以年为单位）的持续肌酸摄入，只要控制在正常范围（小于 20 g），对人体是没有危害的 。尽管肌酸超过 60% 通过肾脏代谢，它对于肾脏，基本没什么影响：科学家甚至曾经找来一个患有肾脏代谢疾病的成年男性，每天摄入高蛋白饮食，外加 20 g 的肌酸，结果发现，这种摄取对于肾脏并没有什么损害 。</P>
<DIV style="TEXT-ALIGN: center"><IMG style="CURSOR: pointer" onClick="window.open('/uploads/allimg/151117/4_151117172426_1.jpg')" border=0 alt="" src="../../图片/62.jpg" width=546 height=388></DIV>
<P>　<STRONG>　肌酸的已知副作用包括：</STRONG></P>
<P>　　腹泻、肠胃道反应等，但一般只在过量摄入的情况下存在；</P>
<P>　　在水分摄入不足的情况下，可能引起脱水反应（比如抽筋）；</P>
<P>　　短期由于导致水分滞留，可能引起 0.8 - 1.6 kg 左右的体重增长；</P></TD></TR>
