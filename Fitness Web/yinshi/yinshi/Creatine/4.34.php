<P></P>
<P style="TEXT-ALIGN: center"><IMG style="FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/65.jpg" width=529 height=361></P>
<P>　　<STRONG>Q：我是新手，需要立刻服</STRONG><STRONG style="FONT-SIZE: 14px">用肌酸吗？</STRONG></P>
<P>　　A：对于新手，首要的任务是掌握正确的训练技巧，制定适合自己的训练计划，以及调整饮食，学习足够的健身知识……请在这一切都走上正轨之后，再考虑补剂问题。</P>
<P>　<STRONG>　Q：我在减脂，还可以服用肌酸吗？</STRONG></P>
<P>　　A：虽然肌酸的主要作用是帮助肌肉增长，减脂期间服用肌酸，也不会有任何副作用。肌酸的另一大作用是可以有效保持瘦体重 ，防止在减脂过程中，肌肉损失过多。</P>
<P>　　当然，如前文所说，短期服用肌酸会造成水分滞留，体重稍微增长，是完全正常的现象，并不是肌酸导致脂肪增加。</P>
<P style="TEXT-ALIGN: center"><IMG style="FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/66.jpg" width=496 height=386></P>
<P>　　<STRONG>Q：女性也能服用肌酸吗？</STRONG></P>
<P>　　A：肌肉增长的机理不分性别，女性当然也能服用。肌酸不是激素。</P>
<P><STRONG>　　Q：哪种肌酸比较好？</STRONG></P>
<P>　　A：现在市场上常见的种类包括：一水肌酸（monohydrate）、盐酸肌酸（HCl）、乙酸乙酯肌酸（diethyl ether）等。后两者又被称为"二代肌酸"，其水溶性以及吸收速度要强于一水肌酸 。但是，它们的主要内容都是肌酸，在摄取总量不变的前提下，对于肌肉增长的影响不会有显着区别。后两者一般价格比较贵</P>
<P>　　只有是正规厂商生产的肌酸，基本没有区别。</P>
<P>　<STRONG>　Q：吃了肌酸肌肉长更快吗？</STRONG></P>
<P>　　A：增长肌肉的最重要因素是饮食和优质的训练――如果你都做的很好，肌酸会给你锦上添花！</P></TD></TR>
