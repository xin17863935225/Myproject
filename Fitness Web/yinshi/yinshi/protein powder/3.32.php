<P></P>
<P style="TEXT-ALIGN: center"><IMG style="BORDER-BOTTOM: 0px; TEXT-ALIGN: center; BORDER-LEFT: 0px; PADDING-BOTTOM: 0px; LINE-HEIGHT: 23px; TEXT-INDENT: 23.99px; MARGIN: 0px 0px 10px; PADDING-LEFT: 0px; PADDING-RIGHT: 0px; FONT-FAMILY: 宋体; MAX-WIDTH: 620px; FONT-SIZE: 14px; VERTICAL-ALIGN: top; BORDER-TOP: 0px; CURSOR: pointer; BORDER-RIGHT: 0px; PADDING-TOP: 0px" border=0 alt="" src="../../图片/39.jpg" width=473 height=296></P>
<P>　　<STRONG>例如乳清分离蛋白</STRONG>（Whey Protein Isolate）一般比乳清蛋白（Whey Protein）有更少甚至完全没有糖分和脂肪，前者的价钱也自然比较贵。</P>
<P>　　功效方面，"增肌粉"不一定比"蛋白质奶粉"好，因为补充品之用处就是补充日常<SPAN style="FONT-SIZE: 14px">饮食。一个很常见的错误，是"瘦底"的健身人士选择了乳清蛋白（Whey Protein）补充品，训练以增肌为目标，然后看不见什么功效。</SPAN></P>
<P>　　乳清蛋白本身的热量通常比较低，而增肌除了训练得宜之后，亦应摄取比日常需要高的热量，否则从乳清蛋白补充品摄取的额外蛋白质，只会用于支持运动训练所需的热量，而不是用在增肌。</P>
<P>　　<STRONG>对了，</STRONG>蛋白质也是身体能量来源，只是一个比起碳水化合物没那么有效的来源。如果你饮用乳清蛋白一段时间而没有明显的增肌效果，同时你的日常饮食没有或者很难作出改变，那么不妨尝试"增肌粉"，或者热量摄取而非蛋白质才是你的增肌障碍。</P>
<P>　<STRONG>　那么如果你的目标是消脂，</STRONG>很可能你自己习惯了在运动训练后饮用"蛋白质奶粉"，以帮助身体复元和预防肌肉流失。这当然是对了，但"增肌粉"在这里就是否就完全没有用处呢？</P>
<P style="TEXT-ALIGN: center"><IMG style="TEXT-ALIGN: center; FONT-SIZE: 14.44px; CURSOR: pointer" border=0 alt="" src="../../图片/47.jpg" width=350 height=467></P>
<P>　　这个未必，因为"增肌粉"的营养配方通常比较平衡，有些人会饮"增肌粉"以取代正餐，同时更有效控制摄向的营养份量和比例，那些针对这个用法的"代餐粉"（Meal Replacement）会是更佳的选择。</P>
<P>　　当然这个"增肌粉"的用法肯定比较少见，因为不少以消脂为目标的健身人士，都有点过份地执着于健身奶粉里的糖分、碳水化合物和脂肪的含量，所以倾向选择例如乳清分离蛋白（Whey Protein Isolate）之类的补充品。</P>
<P>　<STRONG>　总之</STRONG>，虽然补充品产品在设计、生产时已预定了目标用家和用途，但怎样用、用多少，<SPAN style="FONT-SIZE: 14px">都应按个人需要调整的。</SPAN></P></TD></TR>
