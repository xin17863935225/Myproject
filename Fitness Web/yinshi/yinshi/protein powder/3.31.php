<P style="TEXT-ALIGN: center"><IMG style="BORDER-BOTTOM: 0px; TEXT-ALIGN: center; BORDER-LEFT: 0px; PADDING-BOTTOM: 0px; LINE-HEIGHT: 23px; MARGIN: 0px 0px 10px; PADDING-LEFT: 0px; PADDING-RIGHT: 0px; FONT-FAMILY: 宋体; MAX-WIDTH: 620px; FONT-SIZE: 14px; VERTICAL-ALIGN: top; BORDER-TOP: 0px; CURSOR: pointer; BORDER-RIGHT: 0px; PADDING-TOP: 0px" border=0 alt="" src="../../图片/45.jpg" width=521 height=296></P>
<P style="TEXT-ALIGN: left">　　这是我们经常收到的顾客问题：该选"增肌粉"还是"蛋白质奶粉"？</P>
<P>　　<STRONG>增肌粉也叫做增重粉</STRONG></P>
<P>　　首先，两种补充品的最主要分别，是"增肌粉"的总体热量比"蛋白质奶粉"高。而成分方面，"增肌粉"其实如"蛋白质奶粉"一样含有蛋白质，但"增肌粉"为了提高热量，主要会加入碳水化合物和少量脂肪，而碳水化合物和蛋白质通常比例是二比一或一比一。</P>
<P style="TEXT-ALIGN: center"><IMG style="TEXT-ALIGN: center; FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/46.jpg" width=492 height=288></P>
<P>　　<STRONG>那么常听说饮"增肌粉"致肥，是必然的吗？</STRONG></P>
<P>　　"增肌粉"名称当中的"增重"确实可以令人有所担心。现今的"增肌粉"因应市场需求，已有多种低糖低脂的产品，而且每次饮多少份量其实应按自己需要而调整，产品的官方指引通常建议十分高的份量，甚至是相当于正常人一天应吸取热量的一半，胡乱跟从当然会令身体难以消耗而致肥。</P>
<P>　<STRONG>　至于"蛋白粉</STRONG>"也其实不是单一的产品，这类补充品中所含的蛋白质普遍也有几种，有单一蛋白质的补充品，也有复合的。</P>
<P>　　当然，"蛋白质奶粉"主要是提供额外的蛋白质，所以成分方面就应该是蛋白质为主，而糖分、碳水化合物和脂肪的含量会因不同的蛋白质种类而不同，但其实厂方都可以加工调整各成分的比例，这是成本和价钱问题吧。</P>
