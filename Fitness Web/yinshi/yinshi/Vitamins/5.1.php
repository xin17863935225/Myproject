<P>　<STRONG>　3.叶酸</STRONG></P>
<P>　　叶酸B族维生素复合体，对于许多代谢功能，如血液红细胞的形成、蛋白质和氨基酸代谢及利用来说，是一种重要的营养素。</P>
<P>　　食物来源：菠菜、羽衣甘蓝、甜菜、椰菜、玉米、豆角、芽苗，小麦等。</P>
<P>　　<STRONG>4.维生素C</STRONG></P>
<P>　　维生素C是一种神奇的维生素，其作用极为广泛，从抗感冒到抗癌，抵御疾病的侵入，是肌体组织的卫士。维生素C不仅是连接组织间的润滑液的主要成分，还是抗氧化物质，可保护细胞免遭毁坏和防止衰老。</P>
<P>　　要食物来源：蔬菜与水果，如青菜、韭菜、塌棵菜、菠菜，柿子椒等深色蔬菜和花菜，以及柑桔、红果、柚子等水果含量均较高。野生的苋菜、苜蓿、刺梨、沙棘、猕猴桃、酸枣等含量尤其丰富。</P>
