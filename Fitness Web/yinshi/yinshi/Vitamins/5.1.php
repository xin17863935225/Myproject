  <H2 class=article-title>有利于肌肉生长的几种维生素</H2>
    
<TABLE>
  <TBODY>
<TR>
<TD>
<P>&nbsp;　　许多人都知道，身体长肌肉要靠蛋白质，蛋白质还是运动的主要燃料来源，但是肌肉并不是光靠多吃牛肉和鸡胸脯就能形成的。肌肉组织的生长是一个完整的过程，它包括无数的步骤和上百次不同的化学反应，如果这些步骤中的某一个环节脱了轨，肌肉的生长进程就会受阻。而在这个过程中维生素也起着重要的作用。</P>
<P>　　维生素和矿物质是人体生长的催化剂，它对肌体组织有保护、修复和促进生长的作用。虽然维生素在构造身体大厦中不像蛋白质那样扮演着砖头石块的角色，但它也是不可缺少的物质。</P>
<P>　　缺乏维生素和矿物质，肌肉生长就会减缓或受阻。对于经常在健身房锻炼的人，每天必需吸收的基本维生素有13种，矿物质有25种，其中的7种是构造肌肉的重要成分。</P>
<P>　　以下主要介绍对于肌肉生长尤为重要的几种：</P>
<P>　　<STRONG>1.维生素B6</STRONG></P>
<P>　　帮助蛋白质的代谢和血红蛋白的构成，更重要是促进血红细胞的生成。肌肉在运动和代谢过程中需要氧气，血红细胞便是载氧的运输工具，血液的血红细胞含量越多，载氧能力就越强，肌肉的供氧来源就越充足，从而减轻心脏的负荷。</P>
<P>　　食物来源：在小麦谷物和动物肝脏含量比较多。</P>
<P>　　<STRONG>2.维生素B12</STRONG></P>
<P>　　同样能促进血红细胞的生成，它是脱氧核糖核酸（DNA）在肌体生长和肌体修复过程中的推进器，而DNA是蛋白质在合成过程中的重要物质，它指令蛋白质的生成。</P>
<P>　　食物来源：仅存于动物食品中，如鸡蛋、奶制品、鱼和肉；唯一特例是发酵豆豉。</P>
