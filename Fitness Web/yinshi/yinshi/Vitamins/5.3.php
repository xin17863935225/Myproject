<P>&nbsp;　　锻炼一身结实的肌肉是每个男人的梦想。人们都知道，身体长肌肉要靠摄入蛋白质，但是如果光补蛋白，没有维生素B2，补再多蛋白也是白搭。</P>
<P>　　这是因为在众多维生素中，维生素B2最容易缺乏，它参与机体蛋白质的合成代谢，维护皮肤及黏膜的完整，对肌肉发育有重要作用。健身专家张建民指出,维生素B2主要存在于动物内脏中，如肝、肾、心等，牛奶、蛋类和红肉类也含有维生素B2。但由于动物内脏所含脂肪量比较高，也不要超量食用。还可选择牛奶和鸡蛋，普通训练者每天吃4～5个鸡蛋，牛奶不要超过500毫升。</P>
<P>　　而绿叶蔬菜、豆类、野菜及坚果类食品(如核桃、栗子、松子、花生、瓜子等)也含有维生素B2，这些食品对于不爱吃内脏的人来说，也是很好的选择。</P>
