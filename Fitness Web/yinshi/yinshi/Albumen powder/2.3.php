<P>　<STRONG>　酪蛋白：提升你的续航力</STRONG></P>
<P>　　有一句话是这样说的："无蛋白，不肌肉"我们都是健身增肌蛋白质是十分重要的！</P>
<P>　　通常我们所需的蛋白质来源于我们的食物中（瘦肉，奶蛋，鱼虾类）！但是对于需要大量蛋白质的健身者（健美爱好者）来说！要吃下大量的食物无非是个挑战！否则你或多或少都需要一些蛋白质补充剂去助你维持一定的蛋白质摄取量，以促进肌肉生长。</P>
<P>　　但是蛋白质补充剂数量之多，我们应该选择哪种呢？今天要介绍一下酪蛋白Casein Protien。</P>
<P>　<STRONG>　什么是酪蛋白？</STRONG></P>
<P>　　当乳清蛋白Whey Protein成为人人谈论的焦点之际，其实酪蛋白亦理应得到所有健身人士的重视。和乳清蛋白一样，酪蛋白都是由牛奶提炼出来的，因此成份天然，有助肌肉生长及复元。</P>
<P>　　酪蛋白是含有磷的蛋白质，它占牛奶达8成的蛋<SPAN style="FONT-SIZE: 14px">白质，其余2成则是乳清蛋</SPAN><SPAN style="FONT-SIZE: 14px">白。酪蛋白含</SPAN><SPAN style="FONT-SIZE: 14px">有人体必需而不能自己制造的氨基酸，加上其吸收速度较慢，可以在睡前及餐与餐之间进食，有助身体慢慢吸收，加快肌肉生长。</SPAN></P>
<P style="TEXT-ALIGN: center"><IMG style="FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/36.png" width=557 height=336></P>
<P>　<STRONG>　左面白色固体就是酪蛋白</STRONG></P>
<P>　　<STRONG>制造肌肉</STRONG></P>
<P>　　肌肉生长如何增加？就是肌肉制造的速率比肌肉流失的速率高，所以要让肌肉生长加快，你的目标是要让肌肉合成加快，同时减低肌肉的流失。而酪蛋白如何帮到我们加快肌肉生长呢？由于酪蛋白消化需要时间，它会慢慢流进血液，因此酪蛋白对肌肉制造没有太大的帮助，但却十分有效地减慢肌肉流失，所以乳清蛋白和酪蛋白可以说是肌肉增加的最佳拍档。</P>
<P>　　一个Baylor University的研究指出，健身人士如果服用酪蛋白及乳清蛋白，在10星期内，他们的肌肉增长比只服用乳清蛋白的人高很多，所以不要忽略酪蛋白。</P>
<P style="TEXT-ALIGN: center"><IMG style="FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/37.png" width=552 height=316></P>
<P>　　<STRONG>很简单的图解肌肉如何生长</STRONG></P>
<P>　　<STRONG>肌肉复元</STRONG></P>
<P>　　乳清蛋白之所以如此被受追捧，是因为它快被肌肉吸收，因此在训练后的1小时内，要尽快服用乳清蛋白。但大家都知道，身体很多复元的过程都是在熟睡时进行，我们总不能够每两小时起床吃乳清蛋白吧！这时候酪蛋白便大派用场了！由于酪蛋白需要至少3-4小时消化才可以让氨基酸走进血液，到达肌肉细胞，因此大家可以在睡前服用酪蛋白30-40克，让身体在睡觉时持续得到酪蛋白供应养份。</P>
<P>　<STRONG>　此外</STRONG>，酪蛋白能提供一定的饱肚感，所以各位可以当成餐与餐之间的零食，一来可提供蛋白质有助肌肉生长，二来亦减少食欲，避免吃进无益的零食。</P>
