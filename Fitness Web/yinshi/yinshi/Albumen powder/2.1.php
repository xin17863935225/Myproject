<TD>
<DIV style="TEXT-ALIGN: center"><IMG style="CURSOR: pointer" onClick="window.open('/uploads/allimg/161019/4_161019105148_2.jpg')" border=0 alt="" src="../../图片/29.jpg"></DIV>
<P>　<STRONG>　你知道蛋白粉是怎么做出来的吗？</STRONG></P>
<P>　　相信对于广大健身爱好者来说，蛋白粉是我们最耳熟能详的运动营养品！</P>
<P>　　关于蛋白粉，相信大家都有了解一些，但是在网络上还是有很多关于蛋白粉的各种流言蜚语</P>
<P>　　<STRONG>吃了蛋白粉就会长肌肉？蛋白粉是激素，是药？</STRONG></P>
<P>　　今天就来解密蛋白粉的制作以及质检过程~让我们一起科学对待蛋白粉，拒绝神话，更拒绝妖魔化！！</P>
<P>　　知道它的原料和原理，才能吃得更放心</P>
<P style="TEXT-ALIGN: center">&nbsp;</P>
<P>　<STRONG>　1.蛋白粉的源头是牛奶，</STRONG>养殖条件越好，牛奶品质就越好。我们利用高科技吧牛奶中的蛋白质提取出来！</P>
<P>　　一般牛奶的主要营养成分含量如下：</P>
<P>　　水分：87.5%</P>
<P>　　脂肪：3.5～4.2%</P>
<P>　　蛋白质：2.8～3.4%</P>
<P>　　乳糖：4.6～4.8%</P>
<P>　　无机盐：0.7%左右</P>
<P>　<STRONG>　2.杀菌和运送</STRONG></P>
<P>　　通过低温杀菌法，消灭牛奶中的有害细菌，同时保鲜保质。随后将牛奶装入冷藏罐车火速送到奶酪加工厂</P>
<P style="TEXT-ALIGN: center"><IMG style="TEXT-ALIGN: center; FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/30.jpg"></P>
<P>　　<STRONG>3 .分离乳清</STRONG></P>
<P>　　牛奶进行巴氏杀菌，彻底去除有害细菌：先将牛奶煮沸直到其沸点（70-80℃），然后立即在4℃冷却</P>
<P>　　杀菌之后，真正的行动开始了――从牛奶中分离出乳清。</P>
<P>　　在这个过程中，牛奶被分成两部分――液体和固体颗粒。固体颗粒可以进一步加工成奶酪；而液体部分，则含有乳清、脂肪和碳水化合物。</P>
<P>　<STRONG>　4 .冷温纯化</STRONG></P>
<P>　　这一步是一个充满机械运作的过程。上一步产生出来的乳清原液被送进大规模的不锈钢管道网络中，配合陶瓷过滤器，剔除乳清原液中所含有的乳糖，脂肪以及碳水。</P>
<P>　<STRONG>　5. 干燥</STRONG></P>
<P>　　这是加工乳清的最后一步，液体被送进一个干燥器中。</P>
<P>　　这个干燥器同时具有热空气与冷空气，可以干燥乳清蛋白液中的水份，并且从液体中分离固体。</P>
<P style="TEXT-ALIGN: center"><IMG style="TEXT-ALIGN: center; FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/31.jpg"width=446 height=298></P>
<P>　<STRONG>　6. 调出花式口味</STRONG></P>
<P>　　做完上面那一步，我们已经得到了乳清蛋白粉，但是为了更加安全、保证高品质，还需要进一步的品质检测，最重要的是确认其中蛋白质含量的高低。</P>
<P>　　然后加入一些配料使蛋白粉的功能更强大，比如一些维生素，矿物质、消化酶等；同时加入调味剂，调配出最好的口味。</P>
<P><STRONG>　　7. 封装出库到你手</STRONG></P>
<P>　　将乳清蛋白粉、配料、调味剂等一起放入高速混合器中进行完美混合，然后装瓶。</P>
<P>　　经过这些工序只会还要再经历一波品质检查以及抽样检查后，才能出库。</P>
<P>　　如果还能得到第三方公开实验室的品质认证才是好的产品！</P>
<P>　　做完以上的步骤，才算是一罐合格的蛋白粉</P>
