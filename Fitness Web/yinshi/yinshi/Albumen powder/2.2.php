<P>　　<STRONG>健身蛋白质：每天吃多少？</STRONG></P>
<P>　　每天该吃多少蛋白质？这是大多数健身爱好者的疑问！</P>
<P>　　充足的蛋白质能够提供肌肉生长与修复所需的原料，尤其是对於健身与大量运动者而言，每天补充蛋白质来修复受伤的肌肉组织，才能维持健美的身型并远离慢性伤害。</P>
<P>　　<STRONG>但是</STRONG>，蛋白质经过代谢之后会形成含氮废物，而这些废物必须要透过肾脏代谢才能排出体外，所以蛋白质绝非多多益善，过多的蛋白质甚至会埋下肾脏病变的不定时炸弹。</P>
<P>　　该如何拿捏"足够"与"不过量"，就成了补充蛋白质时一定要注意的警戒值。</P>
<P>　　<STRONG>以最简单的方法来记</STRONG>，就是－－多少公斤体重，就吃多少公克的蛋白质。</P>
<P>　　多少体重就搭配多少蛋白质，只能算是概略的原则，但如果说同样体重的健美猛男和久坐办公室的上班族需要等量的蛋白质，怎么听都觉得不对劲。</P>
<DIV style="TEXT-ALIGN: center"><IMG style="CURSOR: pointer" onClick="window.open('/uploads/allimg/161009/4_161009110808_1.jpg')" border=0 alt="" src="../../图片/33.jpg" width=390 height=330></DIV>
<P>　<STRONG>　因此</STRONG>，在精确评估每日蛋白质需求量的时候，就一定要将决定身体耗损程度的"<SPAN style="FONT-SIZE: 14px">活动量"给考虑进来；按照生活型态，可将活动量分成三级。</SPAN></P>
<P>　　再根据自己的活动量来决定每日所需的蛋白质数量（克数），计算方法就是体重的公斤数乘以0.8到1.8不等的蛋白质消耗系数：</P>
<P>　　<SPAN style="COLOR: rgb(255,0,0)">低活动量</SPAN>：体重乘以0.8（以坐姿为主的生活型态）</P>
<P>　　<SPAN style="COLOR: rgb(255,0,0)">中活动量</SPAN>：体重乘以1.3（每日1小时以内的运动量、劳力型工作、怀孕妇女）</P>
<P>　　<SPAN style="COLOR: rgb(255,0,0)">高活动量</SPAN>：体重乘以1.8（高强度的体能与健身活动）有的健美运动员甚至达到2-3克的蛋白质摄取量</P>
<P>　　由这样的计算公式可以看出，蛋白质并不是越多越好，过多的蛋白质已超出人体的吸收能力，最后也等于是过多的热量而必须以脂肪形式储存，反而抵销了健身和运动的成效；简单说来，用不到那么多蛋白质，就别吃太多蛋白质。</P>
<P>　　知道了蛋白质的需求量之后，另一个问题就是：该如何得知自己吃下多少蛋白质了呢？</P>
<P style="TEXT-ALIGN: center"><IMG style="FONT-SIZE: 14px; CURSOR: pointer" border=0 alt="" src="../../图片/34.jpg" width=423 height=446></P>
<P>　　除了乳清蛋白之类的营养补充品可以精确标示出蛋白质含量，认识下列几项常见的高蛋白质食物，人人都能够轻松分配每日、每餐的蛋白质摄取量。</P>
<P>　　<STRONG>1.瘦肉类</STRONG>（猪牛羊），每份大约能提供20-25克的蛋白质。不过要尽量避免油炸的烹调方式</P>
<P>　　<STRONG>2.家禽类</STRONG>：每份大约能提供20克左右的蛋白质。</P>
<P>　<STRONG>　3.鱼虾</STRONG>：每份大约能提供20-25克的蛋白质。</P>
<P>　　<STRONG>4.鸡蛋</STRONG>，每颗约含7克的蛋白质，以蒸蛋、水煮蛋、卤蛋较佳，尽量不要选择用了很多油的炒蛋或荷包蛋。茶叶蛋或皮蛋里的蛋白质通常都已质变，不列为理想的蛋白质来源。</P>
<P>　　<STRONG>5.</STRONG>牛奶等奶制品，每500cc有15克蛋白质。</P>
